export type translationKeys = keyof typeof translations;

export const language = (): 'en' | 'pt' => {
    let language: 'en' | 'pt' = 'en';
    try {
        const stored = localStorage.getItem('language');
        if (stored === 'pt' || stored === 'en') {
            language = stored;
        }
    } catch (e) {
        // localStorage not available, fallback to 'en'
    }
    return language;
};

export const t = (text: translationKeys): string => {
    const lang = language();
    if (lang === 'en') {
        return text;
    }

    return (translations[text] as string) || '???';
};

export const translateMulti = (text: string): string => {
    const lang = language();
    if (lang === 'en') {
        return text;
    }

    // Split text into tokens and separators, preserving order
    // e.g. "A: b" => ["A", ": ", "b"]
    const tokens = text.split(/(\s*[,.;/]+\s*)/);
    return tokens.map(token => {
        if (!translations[token as translationKeys]) {
            return token;
        }
        return t(token as translationKeys);
    }).join('');
};

export const translateMultiSpace = (text: string): string => {
    const lang = language();
    if (lang === 'en') {
        return text;
    }

    const tokens = text.split(/(\s+)/);
    return tokens.map(token => {
        if (!translations[token as translationKeys]) {
            return token;
        }
        return t(token as translationKeys);
    }).join('');
};
